package Chapter2;

import java.util.Scanner;

public class OperationEx1 {

	public static void main(String[] args) {
		int gameScore = 150;
     
    gameScore = gameScore + 5;
    
    System.out.println();
		System.out.println(gameScore);
		System.out.println(++gameScore);
		System.out.println(gameScore++);
		System.out.println(gameScore);
																
		System.out.println(--gameScore);
		System.out.println(gameScore--);
		System.out.println(gameScore);
		System.out.println(gameScore);	
	}
}
